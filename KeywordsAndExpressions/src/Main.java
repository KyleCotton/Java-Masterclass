public class Main {
    public static void main(String[] args) {
	System.out.println("Keywords & Expressions");


	// This entire line is an expression
	int score = 100;

	// The 'score > 99' is the expression here
	if (score > 99) {

	    // The ' "You got the high score" ' is the expression
	    System.out.println("You got the high score");

	    // This entire line is also an expression
	    score = 0;

	}

    }
}
